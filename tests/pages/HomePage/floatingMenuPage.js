class floatingMenuPage extends NativePage {

  //====================================================================================================================
  //Locators
  //====================================================================================================================

  get linkNews() { return $('//div[@id="menu"]/descendant::a[text()="News"]') }

  

  //====================================================================================================================
  //Verification
  //====================================================================================================================


  get selectCheckbox_1() { return $('//form[text()=" checkbox 1"]/descendant::input[@type="checkbox"][@checked][1]') }
 

  //====================================================================================================================
  //Methods
  //====================================================================================================================

  async clickNews() {
    await (await this.linkNews).waitForEnabled({ timeout: 10000 });
    await (await this.linkNews).click();
  }


  //====================================================================================================================
  //Verifications
  //====================================================================================================================

  async verifyFloatingMenuURL() {
    await browser.pause(1000);
    await expect(browser).toHaveUrlContaining('https://the-internet.herokuapp.com/floating_menu#news')
  }

}


module.exports = new floatingMenuPage();
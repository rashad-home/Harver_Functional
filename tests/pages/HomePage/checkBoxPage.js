class checkBoxPage extends NativePage {

  //====================================================================================================================
  //Locators
  //====================================================================================================================

  get linkCheckbox_1() { return $('//form[text()=" checkbox 1"]/descendant::input[@type="checkbox"][1]') }

  

  //====================================================================================================================
  //Verification
  //====================================================================================================================


  get selectCheckbox_1() { return $('//form[text()=" checkbox 1"]/descendant::input[@type="checkbox"][@checked][1]') }
 

  //====================================================================================================================
  //Methods
  //====================================================================================================================

  async clickCheckbox_1() {
    await (await this.linkCheckbox_1).waitForEnabled({ timeout: 10000 });
    await (await this.linkCheckbox_1).click();
  }


  //====================================================================================================================
  //Verifications
  //====================================================================================================================

  async verifyCheckbox_1() {
    await (await this.selectCheckbox_1).waitForDisplayed({ timeout: 10000 });
    await expect(await this.selectCheckbox_1).toBeExisting();
  }

}


module.exports = new checkBoxPage();